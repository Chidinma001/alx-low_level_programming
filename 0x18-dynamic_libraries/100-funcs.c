/**
 * add - func
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - func
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - func
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - func
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - func
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
